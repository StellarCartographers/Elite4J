package elite.dangerous.base;

/**
 * The Class Suit.
 */
public abstract class Suit extends Event {
	
	/**
     * The suit ID.
     */
	public Long suitID;
	
	/**
     * The suit name.
     */
	public String suitName;
	
	/**
     * The suit name localized.
     */
	public String suitName_Localized;
	
	/**
     * The loadout ID.
     */
	public Long loadoutID;
	
	/**
     * The loadout name.
     */
	public String loadoutName;
}
